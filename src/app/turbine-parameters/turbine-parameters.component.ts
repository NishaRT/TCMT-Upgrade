    refreshToken() {
        var self = this;
        self.dataService.getToken()
        .then(
            getTokenResp => {
                if(getTokenResp["access_token"]){
                    self.errorCount = 0;
                    self.globalStorageService.setAccessToken(getTokenResp["access_token"]);
                    if(!self.globalStorageService.getUserInfo()){
                        self.dataService.getUserInfo().then(
                            getUserInfo =>{
                            self.globalStorageService.setUserInfo(getUserInfo);
                            if(!self.globalStorageService.needGRData){
                                self.setListOfFarmsInGlobalService(self.getStaticListOfFarms());
                            }
                            if(!self.globalStorageService.getListOfFarms() && !self.isGLOWFPolling) {
                                    self.isGLOWFPolling = true;
                                    var pollingInterval = self.timesService.getIntervalForId("GetListOfWindFarms");
                                    self.dataService.callPollAndGetResponseWhenSuccess("sendCommand","GetListOfWindFarms","","",0,0,pollingInterval, self.setListOfFarmsInGlobalService.bind(self), null);
                            }
                        });
                    }
                } else {
                    throw new Error("GET_TOKEN_ERROR");
                }
            }
        ).catch(error => {
            console.log(error);
            if(self.errorCount <3){
                self.refreshToken();
            } else {
                self.geUtils.throwErrorMessageInUI("GET_TOKEN_ERROR", false, null, self.geUtils.refreshWindow);
            }
            self.errorCount = self.errorCount + 1;
        });
    }

